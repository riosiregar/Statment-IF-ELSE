#include <iostream>

using namespace std;

int main()
{
	int nilai;
	cout<<"Input Nilai MatKul:"<<endl;
	cin>>nilai;
	
	if (nilai>=60){          //If nilai will be >=60 then it will print Luslus
		cout<<"Lulus";
	}
	else{                    //If nilai will not be >=60 then it will print CIE GAK LULUS  
		cout<<"CIE GAK LULUS";
	}
	return 0;
}
